package academy.devdojo.introducao;

public class Aula06EstruturasDeRepeticao01 {
    public static void main(String[] args) {
        //  while do-while, for
        int count = 0;
        while (count < 11) {
            System.out.println(count);
            count += 1;
        }

        for (int i=0;i<10;i++) {
            System.out.println("For " + i);

        }
    }
}
